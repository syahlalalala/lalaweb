                            <p class="text-start my-5">Tujuh orang siswa SMKN 4 Tasikmalaya beserta satu orang guru pembina ekstrakulikuler Safety Riding mendapatkan beasiswa dari Yayasan Astra Honda Motor (AHM) pada 23 September 2024. Ketujuh orang siswa tersebut merupakan para duta safety riding yang telah diberi pendidikan dan pelatihan secara langsung oleh PT Daya Adicipta Motora dan Yayasan AHM. <br><br> Kepala SMKN 4 Tasikmalaya Kurniawan MPd menjelaskan bahwa beasiswa yang diterima para duta safety riding dan pembina ekstrakulikuler tersebut berupa uang tunai sebesar Rp 1,5 juta untuk masing-masing penerima. “Jadi itu sebagai apresiasi dari Astra Honda Motor bahwa safety riding di SMK Negeri 4 sudah sesuai dengan yang diharapkan oleh mereka,” terangnya kepada Radar. <br><br> Dia menambahkan, sebelum menerima beasiswa tersebut para duta safety riding SMKN 4 Tasikmalaya telah melewati proses seleksi yang dilakukan oleh pihak sekolah. Adapun kriteria yang ditetapkan oleh AHM diantaranya: loyal, memiliki dedikasi yang tinggi, inovatif, dan mau bekerja keras dalam memajukan ekstrakulikuler Safety Riding.</p>
