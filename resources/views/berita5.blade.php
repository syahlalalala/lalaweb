                            <p class="text-start py-5">Kapolsek Cibeureum, AKP Nurrozi, kembali mengambil langkah preventif dengan memberikan penyuluhan kepada siswa-siswi SMKN 4 Kota Tasikmalaya terkait larangan penggunaan knalpot brong dan aktivitas geng motor. Kegiatan ini berlangsung pada Senin (7/10/2024) sebagai bagian dari upaya kepolisian untuk meningkatkan kesadaran generasi muda akan pentingnya tertib lalu lintas dan menghindari perbuatan yang meresahkan masyarakat. <br><br> AKP Nurrozi menegaskan bahwa penggunaan knalpot brong melanggar aturan dan menimbulkan gangguan ketertiban umum. “Knalpot brong tidak hanya mengganggu ketenangan masyarakat, tetapi juga melanggar peraturan lalu lintas yang dapat berujung pada sanksi tegas,” kata Nurrozi. Tak hanya itu, dalam penyuluhan ini Kapolsek juga membahas bahaya geng motor atau berandalan bermotor. Aktivitas semacam ini, menurut Nurrozi, kerap kali berujung pada tindakan kriminal seperti perusakan hingga kekerasan. <br><br> Penyuluhan tersebut diadakan setelah AKP Nurrozi sebelumnya bertugas sebagai pembina upacara di sekolah yang sama. Dalam amanat upacara, ia menyampaikan pesan kepada para siswa agar selalu taat hukum dan menjauhi segala bentuk perilaku yang dapat merugikan diri sendiri maupun orang lain. “Kalian, sebagai generasi muda, punya tanggung jawab besar untuk menjaga perilaku. Jangan sampai terjerumus ke dalam aktivitas negatif seperti geng motor atau pelanggaran lalu lintas,” tegasnya.</p>    
