                            <h2 class="text-center text-primary">DAM Jalin Kerjasama Dengan SMKN 4 Tasikmalaya Terapkan Kurikulum Teknik & Bisnis Sepeda Motor Honda</h2>
                            <p class="text-center">Kamis, 2 Maret 2023 17:53</p>
                            <img class="shadow" src="https://asset-2.tribunnews.com/jabar/foto/bank/images/technical-trainer-sub-dept-head-dam-ade-rohman-kanan-memegang-naskah-mou.jpg" alt="" width="700px" height="400px">
                            <p class="text-start my-5">Sejalan dengan upaya pemerintah mencerdaskan anak bangsa melalui pendidikan vokasi industri, PT Daya Adicipta Motora (DAM) selaku Main Dealer Sepeda Motor dan Suku Cadang Honda di Jawa Barat meresmikan kerjasama dengan SMK Negeri 4 Tasikmalaya yang mengimplementasikan Kurikulum Teknik & Bisnis Sepeda Motor (KTBSM) Astra Honda sekaligus diangkat sebagai Tempat Uji Kompetensi (TUK) versi Astra Honda Motor. <br><br> Peresmian ini dilaksanakan di SMKN 4 Tasikmalaya pada 2 Maret 2023 dengan dihadiri oleh Kepala Cabang Dinas Pendidikan Wilayah XII Tasikmalaya, Kepala Sekolah SMKN 4 Tasikmalaya, serta perwakilan jajaran manajemen PT Astra Honda Motor (AHM) dan DAM. Bergabungnya SMKN 4 Tasikmalaya menambah jumlah SMK yang bekerjasama dengan PT DAM yang kini menjadi 133 SMK Binaan Astra Honda. <br><br> General Manager Technical Service DAM, Denny Budiman mengatakan, peresmian dan penandatanganan MoU kerjasama KTBSM Astra Honda merupakan bentuk kontribusi dalam upaya mendukung program pemerintah dalam menyelenggarakan pendidikan dengan  sistem link and match melalui sinkronisasi kurikulum bersama SMK jurusan teknik sepeda motor dengan kebutuhan dunia kerja. <br><br> Berbagai program dilakukan antara DAM dengan SMKN 4 Tasikmalaya yaitu pengembangan Laboratorium SMK KTBSM Astra Honda, 1 Set Special Tools Honda, Buku Pedoman Reparasi dan Parts Catalog Honda, Modul Ajar Semester 1- 6 dan satu unit sepeda motor matic pemberian PT AHM sebagai sarana praktik kegiatan sekolah.</p>    
